#include<stdio.h>
#include<conio.h>

void main() { /* Multiple by n number */
	int num, i = 1, sum, temp;
   printf("\t\tProgram calculate additional number\n");
   printf("Input number: "); scanf("%d", &num);
   while (i < num ) { /* Execute operation */
   	temp = i * (i+1);
      sum += temp;
      i++;
   }
   if (num < i) printf("Invalid input, Please try again");
   else printf("Summary: %d", sum);
   getch();
}